(
	id ASC
) WITH ( PAD_INDEX = OFF, 
         STATISTICS_NORECOMPUTE = OFF, 
		 IGNORE_DUP_KEY = OFF, 
		 ALLOW_ROW_LOCKS = ON, 
		 ALLOW_PAGE_LOCKS = ON ) 
) 
GO


CREATE TABLE dbo.GpsFile(
	id int IDENTITY(1,1) NOT NULL,
	idGpsInfo int NOT NULL,
